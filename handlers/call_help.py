from aiogram import Router, F
from aiogram.types import CallbackQuery
from keyboards.back import get_back_keyboard

router = Router()


@router.callback_query(F.data == 'help')
async def call_help(call: CallbackQuery):
    help_text = """
    🎨 *ЧТО ТАКОЕ LogoAI?*

    Я помогу создать профессиональный логотип для твоего бренда за минуты, используя AI.

    ━━━━━━━━━━━━━━━━━━━━━━━━━━
    📖 *КАК ПОЛЬЗОВАТЬСЯ:*

    1️⃣ Нажми "🎯 Начать генерацию"
    2️⃣ Опиши свой бренд (например: "кофейня с современным дизайном")
    3️⃣ Выбери стиль и цвет
    4️⃣ Получи готовый логотип

    ━━━━━━━━━━━━━━━━━━━━━━━━━━
    💎 *ЧТО ТАКОЕ КРЕДИТЫ?*

    Каждая генерация логотипа стоит *1 кредит*.
    При регистрации ты получаешь *1 бесплатную генерацию* для теста сервиса.

    ━━━━━━━━━━━━━━━━━━━━━━━━━━
    💰 *КАК КУПИТЬ КРЕДИТЫ?*

    Тарифы (чем больше пакет — тем дешевле одна генерация):
    • 1 генерация — 99₽
    • 2 генерации — 179₽ (~89₽/шт)
    • 3 генерации — 249₽ (~83₽/шт)
    • 5 генераций — 379₽ (~76₽/шт) ⭐
    • 7 генераций — 499₽ (~71₽/шт) 🔥
    • 10 генераций — 649₽ (~65₽/шт)
    • 15 генераций — 899₽ (~60₽/шт)
    • 20 генераций — 1099₽ (~55₽/шт)

    Нажми "💰 Купить кредиты" в главном меню.

    ━━━━━━━━━━━━━━━━━━━━━━━━━━
    """

    await call.message.edit_text(
        text=help_text,
        parse_mode="Markdown",
        reply_markup=get_back_keyboard(),
    )

